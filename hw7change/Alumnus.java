/**
 * Method requirements for an Alumnus.
 * @author John Ryu
 * @version 1.0
 */

public interface Alumnus {

    /**
     * Returns Alma Mater
     * @return almaMater
     */

    String getAlmaMater();

    /**
     * Returns graduation year
     * @return gradYear
     */

    int getGradYear();

    /**
     * Returns Name of Alumnus
     * @return name
     */

    String getName();

    /**
     * Returns class type
     * @return class type
     */

    String getType();

}
